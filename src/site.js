const getInitialLanguage = () => {
  const url = new URL(window.location.href);
  const fromQuery = url.searchParams.get("lang");
  const stored = window.localStorage.getItem("okami-maturity-lang");

  if (fromQuery === "en" || fromQuery === "pt") return fromQuery;
  if (stored === "en" || stored === "pt") return stored;
  return navigator.language.toLowerCase().startsWith("en") ? "en" : "pt";
};

const applyLanguage = (lang) => {
  const dictionary = translations[lang] || translations.pt;
  document.documentElement.lang = lang === "pt" ? "pt-BR" : "en";
  document.body.dataset.lang = lang;
  window.localStorage.setItem("okami-maturity-lang", lang);

  const pageKey = document.body.dataset.page === "docs" ? "docs_" : "";
  const pageTitle = dictionary[`${pageKey}meta_title`] || dictionary.meta_title || "Okami Maturity";
  const pageDescription = dictionary[`${pageKey}meta_description`] || dictionary.meta_description;

  document.title = pageTitle;
  const ogTitle = document.querySelector('meta[property="og:title"]');
  if (ogTitle && pageTitle) ogTitle.setAttribute("content", pageTitle);
  const twitterTitle = document.querySelector('meta[name="twitter:title"]');
  if (twitterTitle && pageTitle) twitterTitle.setAttribute("content", pageTitle);
  const description = document.querySelector('meta[name="description"]');
  if (description && pageDescription) description.setAttribute("content", pageDescription);
  const ogDescription = document.querySelector('meta[property="og:description"]');
  if (ogDescription && pageDescription) ogDescription.setAttribute("content", pageDescription);
  const twitterDescription = document.querySelector('meta[name="twitter:description"]');
  if (twitterDescription && pageDescription) twitterDescription.setAttribute("content", pageDescription);

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.dataset.i18n;
    if (dictionary[key]) node.textContent = dictionary[key];
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((node) => {
    const key = node.dataset.i18nPlaceholder;
    if (dictionary[key]) node.setAttribute("placeholder", dictionary[key]);
  });

  document.querySelectorAll("[data-i18n-aria-label]").forEach((node) => {
    const key = node.dataset.i18nAriaLabel;
    if (dictionary[key]) node.setAttribute("aria-label", dictionary[key]);
  });

  document.querySelectorAll("[data-lang-button]").forEach((button) => {
    const active = button.dataset.langButton === lang;
    button.classList.toggle("is-active", active);
    button.setAttribute("aria-pressed", String(active));
  });
};

document.querySelectorAll("[data-lang-button]").forEach((button) => {
  button.addEventListener("click", () => {
    applyLanguage(button.dataset.langButton);
    if (document.getElementById("samm-question-root")) {
      setTimeout(renderSAMMQuestions, 50);
    }
  });
});

document.querySelectorAll(".showcase-tab").forEach((button) => {
  button.addEventListener("click", () => {
    const image = document.querySelector("[data-showcase-image]");
    if (!image) return;

    document.querySelectorAll(".showcase-tab").forEach((tab) => tab.classList.remove("is-active"));
    button.classList.add("is-active");
    image.src = button.dataset.showcase;
    image.alt = `${button.querySelector("strong")?.textContent || "Okami Maturity"} screenshot`;
    image.parentElement.dataset.accent = button.dataset.accent || "cyan";
  });
});

const renderCopyButton = (button) => {
  button.innerHTML = '<span class="lang lang-pt">copiar</span><span class="lang lang-en">copy</span>';
};

document.querySelectorAll(".install-line button").forEach((button) => {
  button.addEventListener("click", async () => {
    const command = button.parentElement?.querySelector("code")?.textContent?.trim();
    if (!command) return;

    try {
      await navigator.clipboard.writeText(command);
      const lang = document.body.dataset.lang === "en" ? "en" : "pt";
      button.textContent = lang === "en" ? "copied" : "copiado";
      window.setTimeout(() => {
        renderCopyButton(button);
      }, 1400);
    } catch {
      button.textContent = command;
    }
  });
});

const docSections = Array.from(document.querySelectorAll(".docs-hero[id], .doc-section[id]"));
const docNavLinks = Array.from(document.querySelectorAll(".dx-side a[href^='#'], .dx-toc a[href^='#']"));

const setActiveDocSection = (sectionId) => {
  if (!sectionId || !docSections.length) return;

  const section = document.getElementById(sectionId);
  const accent = section?.dataset.docAccent || "orange";
  document.body.dataset.docAccent = accent;

  docNavLinks.forEach((link) => {
    const isActive = link.getAttribute("href") === `#${sectionId}`;
    link.classList.toggle("active", isActive);

    if (isActive) {
      link.setAttribute("aria-current", "true");
      if (link.closest(".dx-side")) {
        link.scrollIntoView({ block: "nearest", inline: "nearest" });
      }
    } else {
      link.removeAttribute("aria-current");
    }
  });
};

const scrollToDocSection = (sectionId) => {
  const section = document.getElementById(sectionId);
  if (!section) return;

  const navOffset = document.querySelector(".dx-nav")?.getBoundingClientRect().height || 72;
  const targetTop = Math.max(0, section.getBoundingClientRect().top + window.scrollY - navOffset - 24);
  const root = document.documentElement;
  const previousScrollBehavior = root.style.scrollBehavior;

  root.style.scrollBehavior = "auto";
  window.scrollTo({ top: targetTop, left: 0, behavior: "auto" });
  root.style.scrollBehavior = previousScrollBehavior;

  window.requestAnimationFrame(() => setActiveDocSection(sectionId));
};

const getCurrentDocSectionId = () => {
  if (!docSections.length) return null;

  const navOffset = document.querySelector(".dx-nav")?.getBoundingClientRect().height || 72;
  const sampleY = window.scrollY + navOffset + Math.min(window.innerHeight * 0.22, 180);
  let current = docSections[0];

  docSections.forEach((section) => {
    if (section.offsetTop <= sampleY) current = section;
  });

  return current.id;
};

const syncDocNavigation = () => {
  setActiveDocSection(getCurrentDocSectionId());
};

if (docSections.length) {
  let scrollFrame = 0;
  const requestDocSync = () => {
    window.cancelAnimationFrame(scrollFrame);
    scrollFrame = window.requestAnimationFrame(syncDocNavigation);
  };

  docNavLinks.forEach((link) => {
    link.addEventListener("click", () => {
      const sectionId = link.getAttribute("href")?.slice(1);
      window.setTimeout(() => setActiveDocSection(sectionId), 30);
    });
  });

  window.addEventListener("scroll", requestDocSync, { passive: true });
  window.addEventListener("resize", requestDocSync);
  window.addEventListener("hashchange", () => scrollToDocSection(window.location.hash.slice(1)));

  const initialSection = window.location.hash.slice(1) || docSections[0].id;
  setActiveDocSection(initialSection);
  if (window.location.hash) {
    window.setTimeout(() => scrollToDocSection(initialSection), 80);
    window.setTimeout(() => scrollToDocSection(initialSection), 220);
  } else {
    window.setTimeout(syncDocNavigation, 120);
  }
}

